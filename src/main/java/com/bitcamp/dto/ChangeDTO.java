package com.bitcamp.dto;

public class ChangeDTO {

	private int last_pno;
	private int new_pno;
	
	
	
	public int getLast_pno() {
		return last_pno;
	}
	public void setLast_pno(int last_pno) {
		this.last_pno = last_pno;
	}
	public int getNew_pno() {
		return new_pno;
	}
	public void setNew_pno(int new_pno) {
		this.new_pno = new_pno;
	}
	
}
